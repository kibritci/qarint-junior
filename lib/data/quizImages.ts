export const UNIQUE_WORDS: string[] = [...new Set(QUIZ_IMAGES.map(q => q.word))].sort();

export function getRandomQuizQuestions(count: number): { image: QuizImage; options: string[]; correct: string }[] {
  const shuffled = [...QUIZ_IMAGES].sort(() => Math.random() - 0.5);
  const selected = shuffled.slice(0, Math.min(count, shuffled.length));

  return selected.map(img => {
    const wrongPool = UNIQUE_WORDS.filter(w => w !== img.word);
    const shuffledWrong = wrongPool.sort(() => Math.random() - 0.5);
    const wrongOptions = shuffledWrong.slice(0, 2);
    const options = [img.word, ...wrongOptions].sort(() => Math.random() - 0.5);
    return { image: img, options, correct: img.word };
  });
}

